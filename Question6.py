# String is “I am a teacher and I love to inspire and teach people”
# Storing string into str1
abc = "I am a teacher and I love to inspire and teach people"
# Performing split() function to split the string
abcd = abc.split()
# Displaying the spring after spliting
print(abcd)
# Removing duplicates by converting them into a set
answer = set(abcd)
# Displaying the set
print(answer)
# Calculating and Displaying number of unique words
print("The number of unique words in the given string is " + str(len(answer)))
